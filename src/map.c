#include "cub3D.h"
#include <stdlib.h>

void	init_map(t_cub *cub)
{
	int		i;
	char	*map_data[] = {
		"1111111111111111111111111111111111111111",
		"1000000000000000000000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1000011111111000000000000000000000000001",
		"1000010000000000000000000000000000000001",
		"10000100N0000000000000000000000000000001",
		"1000010000000000000000000000000000000000",
		"1000011111111000000000000000000000000001",
		"1000000000000000000000000001111111100001",
		"1000000000000000100000000000000000000001",
		"1000000000000000010000000000000000000001",
		"1000000000000000001000000000000000000001",
		"1000000000000000000100000000000000000001",
		"1000000000000000000010000000000000000001",
		"1000000000000000000100000000000000000001",
		"1000000000000000001000000000000000000001",
		"1000000000000000010000000000000000000001",
		"1000000000000000100000000000000000000001",
		"1000000000000000100000000000000000000001",
		"1000000000000000100000000000000000000001",
		"1000000000000000100000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1000000111111111111111000000000000000001",
		"1000000000000000000011111111111111100001",
		"1000000000000000000000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1000000000000000000000000000000000000001",
		"1111111111111111111111111111111111111111",
		NULL
	};

	cub->map.height = 0;
	while (map_data[cub->map.height])
		cub->map.height++;
	cub->map.width = ft_strlen(map_data[0]);
	cub->map.grid = malloc(sizeof(char *) * (cub->map.height + 1));
	i = 0;
	while (i < cub->map.height)
	{
		cub->map.grid[i] = ft_strdup(map_data[i]);
		i++;
	}
	cub->map.grid[i] = NULL;
	i = 0;
	while (i < cub->map.height)
	{
		int j = 0;
		while (j < cub->map.width)
		{
			if (cub->map.grid[i][j] == 'N' || cub->map.grid[i][j] == 'S' ||
				cub->map.grid[i][j] == 'E' || cub->map.grid[i][j] == 'W')
			{
				cub->player.x_pos = j * 20 + 10;
				cub->player.y_pos = i * 20 + 10;
				cub->player.angle = 0.0;
				cub->player.fov = 1.0;
				cub->player.player_dir = 0.0;
				cub->map.grid[i][j] = '0';
				return;
			}
			j++;
		}
		i++;
	}
}
